package step01_기초;

public class SumTest {

	public static void main(String[] args) {
		// +의 기능
		// 1. 더하기
		// 2. 연결하기
		// 3. 단항 연산자
		
		int a = 10;
		int b = 15;
		System.out.println(a+b); // ; : 문장의 끝
		System.out.println("a + b = " + a+b);
		System.out.println("a + b = " + (a+b));
		System.out.println(++a);
	}
}